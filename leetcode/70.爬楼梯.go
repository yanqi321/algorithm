// @lc code=start
func climbStairs(n int) int {
	if n < 3 {
		return n
	}
	s1,s2,s3 := 1,2,3
	for i :=3; i <= n; i++ {
		s3 = s1 + s2
		s1 = s2
		s2 = s3
	}
	return s3
}
